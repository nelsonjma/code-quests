LETTER = {
  ' ' => 0,
  'a' => 1,
  'b' => 2,
  'c' => 3,
  'd' => 4,
  'e' => 5,
  'f' => 6,
  'g' => 7,
  'h' => 8,
  'i' => 9,
  'j' => 10,
  'k' => 11,
  'l' => 12,
  'm' => 13,
  'n' => 14,
  'o' => 15,
  'p' => 16,
  'q' => 17,
  'r' => 18,
  's' => 19,
  't' => 20,
  'u' => 21,
  'v' => 22,
  'w' => 23,
  'x' => 24,
  'y' => 25,
  'z' => 26
}

# make the variable constant
LETTER.freeze

def sum_letter(word)
  # convert word to a list of letters
  word_list = word.scan(/\w/)

  return word_list.map { |l| LETTER[l] }.reduce(:+)
end

puts sum_letter('excellent')
